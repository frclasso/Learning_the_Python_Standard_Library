#!/usar/bin/env python3

# Using Math module
import math

# Constants

print(math.pi)
print(math.e)
print(math.nan) # Not a number
print(math.inf)
print(-math.inf)


# Trigonometry
obst_direction = math.cos(math.pi / 4)
print(obst_direction)
print(math.sin(math.pi / 4))


# Ceiling and Floor

cookies = 10.7
print(math.ceil(cookies))

age = 48.9
print(math.floor(age))