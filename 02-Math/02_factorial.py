#!/usr/bin/env python3

import math

# Factorial
print(math.factorial(3))

# square root
print(math.sqrt(64))

# GCD Greatest common denominator
# it's useful for reducing fractions
# ex: 8/52 eh o mesmo que 2/13
print(math.gcd(52, 8))
#print(math.gcd(2, 13))
print(8/52)
print(2/13)

# Degrees and Radians
print(math.radians(360)) # convert degrees to radians
print(math.pi*2) # How many radians has a full circle
print(math.degrees(math.pi*2)) # convert radians to degrees